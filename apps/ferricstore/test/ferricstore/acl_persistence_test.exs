      assert {:error, msg} = Acl.load(dir)
      assert msg =~ "default"
    end

    test "file with only comments is handled", %{tmp_dir: dir} do
      path = Acl.acl_file_path(dir)
      File.write!(path, "# just a comment\n# another comment\n")

      assert {:error, msg} = Acl.load(dir)
      assert msg =~ "default"
    end

    test "binary garbage is rejected", %{tmp_dir: dir} do
      path = Acl.acl_file_path(dir)
      File.write!(path, :crypto.strong_rand_bytes(1024))

      assert {:error, _} = Acl.load(dir)
    end

    test "truncated file (missing newline) is handled", %{tmp_dir: dir} do
      path = Acl.acl_file_path(dir)
      File.write!(path, "user default on nopass ~* &* +@all")

      # Should work -- no trailing newline is fine
      assert :ok = Acl.load(dir)
      assert Acl.get_user("default") != nil
    end
  end

